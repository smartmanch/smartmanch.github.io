function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        const temp = array[i];
        array[i] = array[j];
        array[j] = temp;
    }
}

// ########## AGGRESSIVE Symbol Cleaning Function ##########
function cleanText(text) {
    // 1. Remove all LaTeX/Math environment delimiters and commands ($, \text{}, \frac, \to, {, }, _, ^, \ etc.)
    let cleaned = text.replace(/(\$\$|\\|{|}|_|\$|\*|\\to|\\rightarrow|\\Rightarrow|\\leftarrow|\\cdot)/g, '');
    
    // 2. Handle specific LaTeX commands like \text{...} and \frac{...}{...}
    // This is a simplified aggressive replacement for common math notation to get plain text
    cleaned = cleaned.replace(/text/g, ''); 
    cleaned = cleaned.replace(/ext/g, ''); 
    cleaned = cleaned.replace(/frac/g, '/'); 

    // 3. Remove remaining single backslash commands
    cleaned = cleaned.replace(/\\([a-zA-Z]+)/g, ''); 
    
    // 4. Remove excessive spaces
    cleaned = cleaned.replace(/(\s\s+)/g, ' '); 
    
    return cleaned.trim();
}


