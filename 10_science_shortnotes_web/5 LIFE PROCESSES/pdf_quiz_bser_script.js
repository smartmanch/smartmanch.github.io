function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        const temp = array[i];
        array[i] = array[j];
        array[j] = temp;
    }
}

// ########## AGGRESSIVE Symbol Cleaning Function ##########
function cleanText(text) {
    // 1. Remove all LaTeX/Math environment delimiters and commands ($, \text{}, \frac, \to, {, }, _, ^, \ etc.)
    let cleaned = text.replace(/(\$\$|\\|{|}|_|\$|\*|\\to|\\rightarrow|\\Rightarrow|\\leftarrow|\\cdot|\\text)/g, '');
    
    // 2. Handle specific LaTeX commands like \text{...} and \frac{...}{...}
    cleaned = cleaned.replace(/frac/g, '/'); 

    // 3. Remove remaining single backslash commands
    cleaned = cleaned.replace(/\\([a-zA-Z]+)/g, ''); 
    
    // 4. Clean up specific chemical formulas for better readability
    cleaned = cleaned.replace(/CO2/g, '‡§ï‡§æ‡§∞‡•ç‡§¨‡§® ‡§°‡§æ‡§á‡§ë‡§ï‡•ç‡§∏‡§æ‡§á‡§°');
    cleaned = cleaned.replace(/H2O/g, '‡§ú‡§≤');
    cleaned = cleaned.replace(/C6H12O6/g, '‡§ó‡•ç‡§≤‡•Ç‡§ï‡•ã‡§ú');
    cleaned = cleaned.replace(/O2/g, '‡§ë‡§ï‡•ç‡§∏‡•Ä‡§ú‡§®');
    cleaned = cleaned.replace(/HCl/g, 'HCl ‡§Ö‡§Æ‡•ç‡§≤');
    cleaned = cleaned.replace(/ATP/g, 'ATP ‡§ä‡§∞‡•ç‡§ú‡§æ');
    
    // 5. Remove excessive spaces
    cleaned = cleaned.replace(/(\s\s+)/g, ' '); 
    
    return cleaned.trim();
}


