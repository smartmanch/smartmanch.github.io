    currentQuestionIndex = 0; // Reset index for quiz
    totalCoins = 0; // Reset score

    document.getElementById('quiz-screen').querySelector('h2').textContent = `üìù ‡§π‡§°‡§º‡§™‡•ç‡§™‡§æ ‡§∏‡§≠‡•ç‡§Ø‡§§‡§æ ‡§™‡§∞ ‡§Ü‡§ß‡§æ‡§∞‡§ø‡§§ ${quizQuestions.length} ‡§µ‡§∏‡•ç‡§§‡•Å‡§®‡§ø‡§∑‡•ç‡§† ‡§™‡•ç‡§∞‡§∂‡•ç‡§®`;


