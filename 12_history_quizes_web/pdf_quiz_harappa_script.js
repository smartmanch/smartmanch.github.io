function finishQuiz() {
    document.getElementById('quiz-screen').style.display = 'none';
    document.getElementById('result-screen').style.display = 'block';
    generateMarkSheet();
}

/**
 * Increments the question index and loads the next question.
 */
function handleNextQuestion() {
    currentQuestionIndex++;
    loadNextQuestion();
}

// ########## Quiz Logic ##########

function loadNextQuestion() {
    if (currentQuestionIndex >= quizQuestions.length) {
        finishQuiz();
        return;
    }

    clickCount = 0;
    coinAwarded = false; 
    document.getElementById('next-button').style.display = 'none';
    const quizArea = document.getElementById('quiz-area');
    quizArea.innerHTML = ''; 
    
    const feedbackDiv = document.getElementById('quiz-screen').querySelector('#feedback-message');
    if (feedbackDiv) feedbackDiv.innerHTML = '';

    const qData = quizQuestions[currentQuestionIndex];
    
    const html = `
        <div class="question-box">
            <h3>‡§™‡•ç‡§∞‡§∂‡•ç‡§® ${currentQuestionIndex + 1}: ${qData.word}</h3>
            <div id="feedback-message" style="margin-bottom: 10px;"></div>
            ${qData.options.map((option, index) => `
                <div class="option" data-index="${index}" onclick="handleOptionClick(this, ${index})">${String.fromCharCode(65 + index)}. ${option}</div>
            `).join('')}
        </div>
    `;
    quizArea.innerHTML = html;
}

function handleOptionClick(element, optionIndex) {
    if (document.getElementById('next-button').style.display !== 'none') {
        return; 
    }

    const qData = quizQuestions[currentQuestionIndex];
    const isCorrect = (optionIndex === qData.answer);
    const feedbackElement = document.getElementById('feedback-message');
    
    clickCount++;

    if (isCorrect) {
        element.classList.add('correct');
        
        if (clickCount === 1) {
            totalCoins += 2;
            coinAwarded = true;
            feedbackElement.innerHTML = `<span style="color: #28a745; font-weight: bold;">**‡§â‡§§‡•ç‡§ï‡•É‡§∑‡•ç‡§ü!** ‡§Ø‡§π ‡§Ü‡§™‡§ï‡§æ ‡§™‡§π‡§≤‡§æ ‡§∏‡§π‡•Ä ‡§™‡•ç‡§∞‡§Ø‡§æ‡§∏ ‡§π‡•à‡•§ ‡§Ü‡§™‡§ï‡•ã 2 ‡§Ö‡§Ç‡§ï ‡§Æ‡§ø‡§≤‡•á! üåü</span>`;
        } else if (clickCount > 1 && !coinAwarded) {
            feedbackElement.innerHTML = `<span style="color: #28a745;">‡§Ø‡§π ‡§∏‡§π‡•Ä ‡§â‡§§‡•ç‡§§‡§∞ ‡§π‡•à, ‡§≤‡•á‡§ï‡§ø‡§® ‡§Ü‡§™‡§ï‡•ã ‡§Ö‡§Ç‡§ï ‡§®‡§π‡•Ä‡§Ç ‡§Æ‡§ø‡§≤‡•á ‡§ï‡•ç‡§Ø‡•ã‡§Ç‡§ï‡§ø ‡§Ø‡§π ‡§Ü‡§™‡§ï‡§æ ‡§™‡§π‡§≤‡§æ ‡§ï‡•ç‡§≤‡§ø‡§ï ‡§®‡§π‡•Ä‡§Ç ‡§•‡§æ‡•§</span>`;
        }
        
        document.querySelectorAll('.option').forEach(opt => opt.onclick = null);
        document.getElementById('next-button').style.display = 'block';
        updateCoinDisplay();

    } else {
        element.classList.add('incorrect');
        
        if (clickCount === 1) {
            coinAwarded = false; 
            feedbackElement.innerHTML = `<span style="color: #dc3545; font-weight: bold;">**‡§ó‡§≤‡§§!** ‡§á‡§∏ ‡§™‡•ç‡§∞‡§∂‡•ç‡§® ‡§ï‡•á ‡§≤‡§ø‡§è ‡§ï‡•ã‡§à ‡§Ö‡§Ç‡§ï ‡§®‡§π‡•Ä‡§Ç ‡§Æ‡§ø‡§≤‡•á‡§ó‡§æ‡•§ ‚ùå</span>`;
            
            // Allow clicking only the correct option next, to show the right answer
            document.querySelectorAll('.option').forEach(opt => opt.onclick = null);
            document.querySelector(`.option[data-index="${qData.answer}"]`).onclick = function() {
                handleOptionClick(this, qData.answer);
            };
        } else {
            feedbackElement.innerHTML = `<span style="color: #dc3545;">‡§Ö‡§≠‡•Ä ‡§≠‡•Ä ‡§ó‡§≤‡§§ ‡§π‡•à‡•§ ‡§Ö‡§¨ ‡§ï‡•ã‡§à ‡§Ö‡§Ç‡§ï ‡§®‡§π‡•Ä‡§Ç ‡§Æ‡§ø‡§≤‡•á‡§ó‡§æ‡•§ ‚ùå</span>`;
            element.onclick = null; // Disable clicking this incorrect option again
        }
    }
}

